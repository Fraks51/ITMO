#define _CRT_SECURE_NO_WARNINGS
#include <iostream>
typedef long long ll;
using namespace std;
 
class Stack {
public:
    ll value;
    Stack * next;
    Stack(ll x, Stack *a)
        //:value(x), next(a)
    {
        value = x;
        next = a;
    }
};
 
Stack * head = (Stack*)malloc(sizeof(Stack));
 
void push(ll x) {
    Stack *new_head = (Stack*)malloc(sizeof(Stack));
    new_head->value = x;
    new_head->next = head;
    head = new_head;
}
 
ll pop() {
    ll answer = head->value;
    head = head->next;
    return answer;
}
 
